-- PACKROVE CRM V2 -> V9.5.2 兼容升级
-- 作用：保留旧客户数据，补齐新版字段和表，不删除现有数据。
-- 可重复执行。
-- 请在 Supabase Dashboard -> SQL Editor -> New query 中完整执行。

begin;

create extension if not exists pgcrypto;

-- 1. 补齐旧 customers 表缺少的新版字段
alter table public.customers add column if not exists platform text default '';
alter table public.customers add column if not exists country text default '';
alter table public.customers add column if not exists city text default '';
alter table public.customers add column if not exists website text default '';
alter table public.customers add column if not exists instagram text default '';
alter table public.customers add column if not exists email text default '';
alter table public.customers add column if not exists contact_name text default '';
alter table public.customers add column if not exists customer_type text default '';
alter table public.customers add column if not exists customer_pool text default '未建联';
alter table public.customers add column if not exists target_product text default '';
alter table public.customers add column if not exists recommended_product text default '';
alter table public.customers add column if not exists outreach_angle text default '';
alter table public.customers add column if not exists level text default 'C';
alter table public.customers add column if not exists touch_status text default '未联系';
alter table public.customers add column if not exists grade_reason text default '';
alter table public.customers add column if not exists requirement text default '';
alter table public.customers add column if not exists quantity text default '';
alter table public.customers add column if not exists budget text default '';
alter table public.customers add column if not exists concern text default '';
alter table public.customers add column if not exists next_action text default '';
alter table public.customers add column if not exists next_follow date;
alter table public.customers add column if not exists probability integer default 0;
alter table public.customers add column if not exists notes text default '';
alter table public.customers add column if not exists last_contact date;
alter table public.customers add column if not exists profile text default '';
alter table public.customers add column if not exists stage text default '需求确认';
alter table public.customers add column if not exists created_at timestamptz default now();
alter table public.customers add column if not exists updated_at timestamptz default now();

-- 2. 把V2字段内容迁移到V9.5字段
do $$
begin
  if exists (
    select 1 from information_schema.columns
    where table_schema='public' and table_name='customers' and column_name='grade'
  ) then
    execute $q$
      update public.customers
      set level = case
        when grade in ('A','B','C') then grade
        else coalesce(nullif(level,''),'C')
      end
      where level is null or level='' or level='C'
    $q$;
  end if;

  if exists (
    select 1 from information_schema.columns
    where table_schema='public' and table_name='customers' and column_name='products'
  ) then
    execute $q$
      update public.customers
      set target_product = coalesce(nullif(target_product,''), products, ''),
          recommended_product = coalesce(nullif(recommended_product,''), products, '')
    $q$;
  end if;
end $$;

update public.customers
set customer_pool =
  case
    when customer_pool in ('未建联','已建联') then customer_pool
    when coalesce(touch_status,'') in ('已回复','一触完成','二触完成','三触完成') then '已建联'
    else '未建联'
  end;

update public.customers
set touch_status =
  case
    when touch_status in ('未联系','一触完成','二触完成','三触完成','已回复') then touch_status
    when customer_pool='已建联' then '已回复'
    else '未联系'
  end;

update public.customers
set level = case when level in ('A','B','C') then level else 'C' end,
    stage = coalesce(nullif(stage,''),'需求确认'),
    updated_at = coalesce(updated_at,now());

-- 3. 新版沟通时间轴
create table if not exists public.timeline (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid(),
  customer_id bigint not null references public.customers(id) on delete cascade,
  event_date date not null default current_date,
  event_type text default '跟进',
  content text not null,
  created_at timestamptz not null default now()
);

-- 4. 新版报价表（V2原来叫 quotes）
create table if not exists public.quotations (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid(),
  customer_id bigint not null references public.customers(id) on delete cascade,
  quote_no text not null,
  product text default '',
  quantity numeric default 0,
  unit_price numeric default 0,
  amount numeric default 0,
  currency text default 'USD',
  issue_date date default current_date,
  valid_until date,
  status text default '草稿',
  notes text default '',
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 5. 新版PI表（V2原来叫 pis）
create table if not exists public.pi_documents (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid(),
  customer_id bigint not null references public.customers(id) on delete cascade,
  quotation_id bigint references public.quotations(id) on delete set null,
  pi_no text not null,
  amount numeric default 0,
  currency text default 'USD',
  payment_method text default 'T/T',
  issue_date date default current_date,
  status text default '待确认',
  created_at timestamptz default now()
);

-- 6. 补齐V2 orders表缺少的新版字段
alter table public.orders add column if not exists pi_id bigint references public.pi_documents(id) on delete set null;
alter table public.orders add column if not exists order_no text;
alter table public.orders add column if not exists product text default '';
alter table public.orders add column if not exists quantity text default '';
alter table public.orders add column if not exists currency text default 'USD';
alter table public.orders add column if not exists payment_status text default '待付款';
alter table public.orders add column if not exists production_status text default '待生产';
alter table public.orders add column if not exists delivery_date date;
alter table public.orders add column if not exists updated_at timestamptz default now();

do $$
begin
  if exists (
    select 1 from information_schema.columns
    where table_schema='public' and table_name='orders' and column_name='number'
  ) then
    execute $q$
      update public.orders
      set order_no = coalesce(nullif(order_no,''), nullif(number,''), 'V2-PO-' || id::text)
    $q$;
  else
    update public.orders
    set order_no = coalesce(nullif(order_no,''), 'V2-PO-' || id::text);
  end if;

  if exists (
    select 1 from information_schema.columns
    where table_schema='public' and table_name='orders' and column_name='status'
  ) then
    execute $q$
      update public.orders
      set production_status = coalesce(nullif(production_status,''), status, '待生产')
    $q$;
  end if;
end $$;

