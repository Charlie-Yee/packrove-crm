-- 8. 将V2旧报价和PI复制到新版表（避免重复）
do $$
begin
  if to_regclass('public.quotes') is not null then
    execute $q$
      insert into public.quotations
        (user_id,customer_id,quote_no,amount,status,notes,created_at,updated_at)
      select
        q.user_id,q.customer_id,
        coalesce(nullif(q.number,''),'V2-Q-' || q.id::text),
        coalesce(q.amount,0),coalesce(q.status,'草稿'),coalesce(q.notes,''),
        coalesce(q.created_at,now()),coalesce(q.created_at,now())
      from public.quotes q
      where not exists (
        select 1 from public.quotations n
        where n.user_id=q.user_id
          and n.quote_no=coalesce(nullif(q.number,''),'V2-Q-' || q.id::text)
      )
    $q$;
  end if;

  if to_regclass('public.pis') is not null then
    execute $q$
      insert into public.pi_documents
        (user_id,customer_id,pi_no,amount,status,created_at)
      select
        p.user_id,p.customer_id,
        coalesce(nullif(p.number,''),'V2-PI-' || p.id::text),
        coalesce(p.amount,0),coalesce(p.status,'待确认'),
        coalesce(p.created_at,now())
      from public.pis p
      where not exists (
        select 1 from public.pi_documents n
        where n.user_id=p.user_id
          and n.pi_no=coalesce(nullif(p.number,''),'V2-PI-' || p.id::text)
      )
    $q$;
  end if;
end $$;

-- 9. 更新时间触发器
create or replace function public.set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at=now();
  return new;
end $$;

drop trigger if exists customers_updated_at on public.customers;
drop trigger if exists customers_set_updated_at on public.customers;
create trigger customers_updated_at
before update on public.customers
for each row execute function public.set_updated_at();

drop trigger if exists quotations_updated_at on public.quotations;
create trigger quotations_updated_at
before update on public.quotations
for each row execute function public.set_updated_at();

-- 10. 索引
create index if not exists customers_next_follow_idx on public.customers(next_follow);
create index if not exists customers_pool_idx on public.customers(customer_pool);
create index if not exists customers_stage_idx on public.customers(stage);
create index if not exists timeline_customer_idx on public.timeline(customer_id,event_date desc);

-- 11. RLS：每个登录账号只访问自己的数据
alter table public.customers enable row level security;
alter table public.timeline enable row level security;
alter table public.quotations enable row level security;
alter table public.pi_documents enable row level security;
alter table public.orders enable row level security;
alter table public.shipments enable row level security;

do $$
declare t text;
begin
  foreach t in array array[
    'customers','timeline','quotations','pi_documents','orders','shipments'
  ] loop
    execute format('drop policy if exists authenticated_all on public.%I',t);
    execute format('drop policy if exists own_select on public.%I',t);
    execute format('drop policy if exists own_insert on public.%I',t);
    execute format('drop policy if exists own_update on public.%I',t);
    execute format('drop policy if exists own_delete on public.%I',t);

    execute format(
      'create policy own_select on public.%I for select to authenticated using (auth.uid()=user_id)',t
    );
    execute format(
      'create policy own_insert on public.%I for insert to authenticated with check (auth.uid()=user_id)',t
    );
    execute format(
      'create policy own_update on public.%I for update to authenticated using (auth.uid()=user_id) with check (auth.uid()=user_id)',t
    );
    execute format(
      'create policy own_delete on public.%I for delete to authenticated using (auth.uid()=user_id)',t
    );
  end loop;
end $$;

grant usage on schema public to authenticated;
grant select,insert,update,delete on public.customers to authenticated;
grant select,insert,update,delete on public.timeline to authenticated;
grant select,insert,update,delete on public.quotations to authenticated;
grant select,insert,update,delete on public.pi_documents to authenticated;
grant select,insert,update,delete on public.orders to authenticated;
grant select,insert,update,delete on public.shipments to authenticated;
grant usage,select on all sequences in schema public to authenticated;

commit;

-- 通知 Supabase/PostgREST 立即刷新表结构缓存
notify pgrst, 'reload schema';
